package OfficeHour;

public class Array2 {
	public static void main(String[] args) {
		
		int [][] nums = {	{10,20,30},
							{40,50,60},
							{70,80}	};	
		//                            3
		for(int arrIndex=0;arrIndex<nums.length;arrIndex++) {
			for(int elementIndex =0; elementIndex < nums[arrIndex].length;elementIndex++) {
			System.out.print(nums[arrIndex][elementIndex] + " ");
			}
			System.out.println();
			}
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
