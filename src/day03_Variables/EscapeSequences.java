package day03_Variables;

public class EscapeSequences {
	/*
	 \n (\r): starts from a new line
	 \t: horizontal tab in word
	 \\: prints a back slash
	 \': prints single quote
	 \": prints double qoute
	 
	 
	 /:back slash
	 \: front slash
	 */
	public static void main(String[] args) {
		System.out.println("Hello Cybertek\n Batch12 students");
		
		System.out.print("Tomorrow is off \n");
		System.out.print("True \n");
		System.out.print("False \n\n\n");
		
		System.out.println("\tClass starts tomorrow");
		
		System.out.println("\\nurzat");
		System.out.println("\'Java");
		
		System.out.println("Book is called \"Game of Throme\" ");
	}
	
	
	
	
}
