package day19_forLoop;

public class ForLoop {
	public static void main(String[] args) {
		/*
		 for(initialization; condition; iterator){
		 statements that we wanna repeat
		 
		 statements within loops body is repeated 
		 until condition becomes false
		 
		 initialization: usually the times of the number that we want execute the loop
		 starting point 
		 Ex: int i=0;
		 
		 condition: condition is created based on the initialization
		 Ex:  i<10
		 
		 iterator: co-related to the condition, runs long enough and either increases or
		 decreases the value of initialization, the eventually condition becomes false
		 Ex: i++, i +=2 ..
		 
		 }
		 */
		for(int i=10; i >0; i--) {
			System.out.println("Value of i is "+i);
		}
		System.out.println("====================");
		 
			for(int i=5; i <10; i++) {
				System.out.println("Value of i is "+i);
			}
		
		System.out.println("===============");
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
